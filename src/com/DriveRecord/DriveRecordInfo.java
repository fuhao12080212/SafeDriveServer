package com.DriveRecord;

public class DriveRecordInfo {
	private int RecordId;
	private String Time;
	private String DangerLongitude;
	private String DangerLatitude;
	private String DangerSpeed;
	private String RecordType;

	public int getRecordId() {
		return RecordId;
	}

	public void setRecordId(int recordId) {
		RecordId = recordId;
	}

	public String getTime() {
		return Time;
	}

	public void setTime(String time) {
		Time = time;
	}

	public String getDangerLongitude() {
		return DangerLongitude;
	}

	public void setDangerLongitude(String dangerLongitude) {
		DangerLongitude = dangerLongitude;
	}

	public String getDangerLatitude() {
		return DangerLatitude;
	}

	public void setDangerLatitude(String dangerLatitude) {
		DangerLatitude = dangerLatitude;
	}

	public String getDangerSpeed() {
		return DangerSpeed;
	}

	public void setDangerSpeed(String dangerSpeed) {
		DangerSpeed = dangerSpeed;
	}

	public String getRecordType() {
		return RecordType;
	}

	public void setRecordType(String recordType) {
		RecordType = recordType;
	}

}
